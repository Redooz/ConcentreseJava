    private void mostrarScores() {
        List<Usuario> userScores = leerScores.leer();
        String text = "";
        
        if(userScores.isEmpty()){
            txtScores.setText("No hay puntajes a√∫n, ve y juega");
        } else {
            for (Usuario i : userScores) {
                text = i.getNombre()+" "+i.getPuntaje();
                txtScores.setText(text+"\n");
            }
        }
    }
